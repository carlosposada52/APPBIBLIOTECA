  CREATE OR REPLACE EDITIONABLE TRIGGER "C##ADMIN"."TRG_GENERAR_MULTA_RETRASO" 
AFTER UPDATE OF FECHA_DEVOLUCION_REAL ON PRESTAMO
FOR EACH ROW
DECLARE
    v_dias_retraso NUMBER;
    v_monto_multa NUMBER;
    v_existe_multa NUMBER;
    v_id_multa NUMBER;
BEGIN
    -- Verificar si se registró una fecha de devolución real
    IF :NEW.FECHA_DEVOLUCION_REAL IS NOT NULL THEN
        -- Calcular días de retraso
        v_dias_retraso := :NEW.FECHA_DEVOLUCION_REAL - :NEW.FECHA_DEVOLUCION;

        -- Solo proceder si hay retraso (días > 0)
        IF v_dias_retraso > 0 THEN
            -- Verificar si ya existe multa para este préstamo
            SELECT COUNT(*) INTO v_existe_multa 
            FROM MULTA 
            WHERE IDPRESTAMO = :NEW.ID_PRESTAMO;

            -- Si no existe multa previa, crear una nueva
            IF v_existe_multa = 0 THEN
                -- Calcular monto (ejemplo: 10 por día de retraso)
                v_monto_multa := v_dias_retraso * 10;

                -- Insertar multa (sin especificar el ID)
                INSERT INTO MULTA (
                    MONTO,
                    FECHAGENERADA,
                    IDPRESTAMO
                ) VALUES (
                    v_monto_multa,
                    SYSDATE,
                    :NEW.ID_PRESTAMO
                ) RETURNING ID_MULTA INTO v_id_multa;

                -- Si el retraso es mayor a 7 días, registrar penalización
                IF v_dias_retraso > 7 THEN
                    INSERT INTO USUARIOS_MORA (
                        ID_USUARIO_MORA,
                        IDMULTA,
                        ESTADOPENALIZACION,
                        FECHA_REGISTRO
                    ) VALUES (
                        :NEW.ID_USUARIO,
                        v_id_multa, -- Usamos el ID retornado
                        1, -- 1 = Activa
                        SYSDATE
                    );


                END IF;


            END IF;
        END IF;
    END IF;
    END;

